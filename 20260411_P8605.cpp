#include <bits/stdc++.h>
using namespace std;
const int kL = 1e4 + 1;
int n, m;
vector<int> a[kL];
int main() {
  cin >> n >> m;
  for (int i = 1; i <= m; i++) {
    int u, v;
      cin >> u >> v, a[u].push_back(v), a[v].push_back(u);
  }
  for (int i = 1; i <= n; i++) {
    for (int j = 1; j <= n; j++) {
      if (j != i) {
        cout << i << ' ' << j << '\n';
      }
    }
  }
  return 0;
}

