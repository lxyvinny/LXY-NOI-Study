#include <bits/stdc++.h>
using namespace std;

long long a, b, c, x[25][25][25], y[25][25][25];

// 定义递归函数 W：计算题目要求的数值，并使用记忆化技巧
long long W(long long a, long long b, long long c) {
  // 基础边界判断：如果任意一个数小于等于 0，直接返回 1
  if (a <= 0 || b <= 0 || c <= 0) {
    return 1;  // 满足条件 1
  } else if (a > 20 || b > 20 || c > 20) {  // 范围缩减判断：如果任意一个数大于 20，强制转为 20 计算
    return W(20, 20, 20);  // 满足条件 2，防止数组越界并提高效率
  }

  // 记忆化检查：查看标记清单 y，确认当前 a, b, c 的组合是否已经计算过
  if (y[a][b][c]) {
    return x[a][b] [c];  // 如果计算过，直接从结果 x 中取值返回，不再重复计算
  }

  long long t;  // 定义临时变量 t，用于存储本次递归计算的结果

  if (a < b && b < c) {
    t = W(a, b, c - 1) + W(a, b - 1, c - 1) - W(a, b - 1, c);
  } else {
    t = W(a - 1, b, c) + W(a - 1, b - 1, c) + W(a - 1, b, c - 1) - W(a - 1, b - 1, c - 1);
  }

  // 记忆化存储：将算出来的结果 t 存入 x[a][b][c]，并在数组 y[a][b][c] 上标记。
  x[a][b][c] = t;  // 存入结果
  y[a][b][c] = 1;  // 标记已完成

  return t;
}

int main() {
  while (1) {
    cin >> a >> b >> c;

    if (a == -1 && b == -1 && c == -1) {
      break;
    }

    cout << "w(" << a << ", " << b << ", " << c << ") = " << W(a, b, c) << '\n';
  }

  return 0;
}

/*
状态记录机制：提出使用两个数组（或一个结构体数组）来记录状态。
一个数组（布尔型）标记某个状态是否已被计算过，另一个数组存储该状态的计算结果。

剪枝优化逻辑：在递归函数入口处，先检查当前状态是否已计算过。
若已计算，直接返回存储的结果；若未计算，则进行递归计算，并将结果存入数组后再返回。
*/